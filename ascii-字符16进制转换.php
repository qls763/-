function hexToStr($arrs){
	$arr = array();
	foreach($arrs as $k=>$v){
		$arr[] = pack("H*",bin2hex($v)); // 用pack函数可以恢复字符串  
	}

	return $arr;
}

function strToHex($arrs){
	$arr = array();
	foreach($arrs as $k=>$v){
		$arr[] =  bin2hex($v); // 转16进制  
	}
	return $arr;
}

function hexToChar($arrs){
	$arr = array();
	foreach($arrs as $k=>$v){
		$arr[] =  chr($v); // 转acsii 
	}
	return $arr;
}

function toString($arrs){
	$str = '';
	foreach($arrs as $k=>$v){
		$str.=$v;
	}
	return $str;
}
